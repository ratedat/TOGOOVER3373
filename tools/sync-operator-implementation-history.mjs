import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { extractImplementationHistory, mergeImplementationHistory } from "../app/domain/operator-implementation-history.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const defaultProjectRoot = path.resolve(__dirname, "..");

function readArg(args, name, fallback = null) {
  const index = args.indexOf(name);
  if (index < 0) return fallback;
  return args[index + 1] || fallback;
}

function decodePreHtml(value) {
  return String(value || "")
    .replace(/&#x([0-9a-f]+);/gi, (_, hex) => String.fromCodePoint(Number.parseInt(hex, 16)))
    .replace(/&#(\d+);/g, (_, code) => String.fromCodePoint(Number.parseInt(code, 10)))
    .replace(/&quot;/g, '"')
    .replace(/&#039;|&apos;/g, "'")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&amp;/g, "&");
}

async function readJson(file) {
  return JSON.parse(await fs.readFile(file, "utf8"));
}

async function writeJson(file, value) {
  await fs.writeFile(file, `${JSON.stringify(value, null, 2)}\n`, "utf8");
}

function localIsoDate(date = new Date()) {
  const year = date.getFullYear();
  const month = String(date.getMonth() + 1).padStart(2, "0");
  const day = String(date.getDate()).padStart(2, "0");
  return [year, month, day].join("-");
}

async function fetchWikiSource(page) {
  const sourceUrl = `https://arknights.wikiru.jp/?cmd=source&page=${encodeURIComponent(page)}`;
  const response = await fetch(sourceUrl);
  if (!response.ok) throw new Error(`Failed to fetch ${sourceUrl}: ${response.status}`);
  const html = await response.text();
  const match = html.match(/<pre id="source">(?<source>[\s\S]*?)<\/pre>/);
  if (!match?.groups?.source) throw new Error(`Could not find source pre for ${page}`);
  return decodePreHtml(match.groups.source);
}

async function main() {
  const projectRoot = path.resolve(readArg(process.argv, "--project-root", readArg(process.argv, "-ProjectRoot", defaultProjectRoot)));
  const dataDir = path.join(projectRoot, "data");
  const config = await readJson(path.join(dataDir, "wikiru-operator-sources.json"));
  const sourcePage = config.implementationHistoryPage || "オペレーター実装履歴";
  const sourceUrl = config.implementationHistoryUrl || "https://arknights.wikiru.jp/index.php?オペレーター実装履歴";
  const source = await fetchWikiSource(sourcePage);
  const history = extractImplementationHistory(source);
  if (!history.length) throw new Error(`No implementation history rows parsed from ${sourcePage}`);

  const operatorDoc = await readJson(path.join(dataDir, "operators.json"));
  const mergeResult = mergeImplementationHistory(operatorDoc.operators || [], history);
  const output = {
    version: 1,
    meta: {
      generatedAt: localIsoDate(),
      source: "arknights.wikiru.jp operator implementation history",
      sourcePage,
      sourceUrl,
      note: "Generated by tools/sync-operator-implementation-history.mjs. It is merged into master operator data at runtime.",
    },
    summary: mergeResult.summary,
    history,
    unmatchedHistoryRows: mergeResult.unmatchedHistoryRows,
  };
  await writeJson(path.join(dataDir, "operator-implementation-history.json"), output);
  console.log(`Operator implementation history synced: rows=${history.length} matched=${mergeResult.summary.matchedOperators} unmatched=${mergeResult.summary.unmatchedHistoryRows}`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exitCode = 1;
});
